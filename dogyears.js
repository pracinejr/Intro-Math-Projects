const myAge = 29;
// My age 
let earlyYears = 2;
// First 2 years of a dog's life
earlyYears *= 10.5;

let laterYears = myAge - 2;
laterYears *= 4;

console.log(earlyYears);
console.log(laterYears);


let myAgeInDogYears = earlyYears + laterYears;
// this is the sum of laterYears and earlyYears added together

let myName = 'Peter Racine Jr'.toLowerCase();
// Type my name. Use .toLowerCase to make it lower case. Store as var myName
console.log(`My name is ${myName}. I am ${myAgeInDogYears} years old in dog years.`);



